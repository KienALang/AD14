package ad14.models.entities;


public class LoaiCanhSat {

    private long idLoaiCanhSat;
    private String tenLoaiCanhSat;
    private String vietTat;


    public long getIdLoaiCanhSat() {
        return idLoaiCanhSat;
    }

    public void setIdLoaiCanhSat(long idLoaiCanhSat) {
        this.idLoaiCanhSat = idLoaiCanhSat;
    }


    public String getTenLoaiCanhSat() {
        return tenLoaiCanhSat;
    }

    public void setTenLoaiCanhSat(String tenLoaiCanhSat) {
        this.tenLoaiCanhSat = tenLoaiCanhSat;
    }


    public String getVietTat() {
        return vietTat;
    }

    public void setVietTat(String vietTat) {
        this.vietTat = vietTat;
    }

}
